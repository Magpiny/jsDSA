//Array deletion is 8mplemented using pop() and shift()

//pop() deletes from the end
let array  = [1, 2, 4, 'black', 'justin']
array.pop()  //returns >> [1,2,3, 'black']

//shift() deletes from the front
array.shift() // >>[2, 3, 'black'] returns 2...which is deketed

console.log(array.shift());
